# Under Number Data Type

no = 11    #int

marks = 90.78  #float

value = 8 + 5j   # Complex

print(type(no))   #int
print(type(marks)) #float
print(type(value)) #complex

print(value)     #  8 + 5j 



# Boolean Data Type

gender = True

print(gender)
print(type(gender))


# None Data Type

demo = None

print(demo)   #None

print(type(demo)) #NoneType
